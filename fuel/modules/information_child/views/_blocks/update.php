			type: "POST",  
			url	: "<?php echo fuel_url('information_child/updatesummary');?>/",  
			data: dataString,
			success: function(msg){  
				$("#check_bar").html(msg);
				setTimeout(function() {
					$("#check_bar").hide();
				}, 5000);
			}  
		});  
	}
